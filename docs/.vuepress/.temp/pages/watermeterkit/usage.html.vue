<template><div><h1 id="usage-guide" tabindex="-1"><a class="header-anchor" href="#usage-guide"><span>Usage Guide</span></a></h1>
<p>The WaterMeterKit V2.0 allows you to monitor your water consumption in real-time through Home Assistant.</p>
<h2 id="home-assistant-energy-dashboard" tabindex="-1"><a class="header-anchor" href="#home-assistant-energy-dashboard"><span>Home Assistant Energy dashboard</span></a></h2>
<p>To view historical water consumption data and integrate it into the Home Assistant Energy Dashboard, follow these steps:</p>
<ol>
<li><strong>Open Home Assistant</strong> and navigate to <strong>Settings</strong>.</li>
<li>Go to <strong>Dashboards</strong>.</li>
<li>Select <strong>Energy Dashboard</strong>.</li>
<li>Under the <strong>Water Consumption</strong> section, click on <strong>Add Water Source</strong>.</li>
<li>Choose the <strong>Water Meter Total Consumption Entity</strong>.</li>
</ol>
<p>After approximately 5 minutes, your water consumption data will be automatically displayed in the Home Assistant Energy Dashboard.</p>
